object Functions {
  def multiply(x: Int, y: Int) : Unit = {
    println(s"Multiplying: ${x} * ${y} = ${x * y}")
  }

  def add1(x: Int): Unit = {
    println(s"Adding: ${x} + 1 = ${x + 1}")
  }

  def subtract1(x: Int): Unit = {
    println(s"Subtract: ${x} - 1 = ${x - 1}")
  }

  def square(x: Int) : Unit = {
    println(s"Subtract: ${x} * ${x} = ${x * x}")
  }
}




